package com.aura.task4web.entity;

public class PvalueDistributed {

    private Integer id;
    private String cateId;
    private Integer pvalue;
    private Integer pvalueNums;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCateId() {
        return cateId;
    }

    public void setCateId(String cateId) {
        this.cateId = cateId;
    }

    public Integer getPvalue() {
        return pvalue;
    }

    public void setPvalue(Integer pvalue) {
        this.pvalue = pvalue;
    }

    public Integer getPvalueNums() {
        return pvalueNums;
    }

    public void setPvalueNums(Integer pvalueNums) {
        this.pvalueNums = pvalueNums;
    }
}
