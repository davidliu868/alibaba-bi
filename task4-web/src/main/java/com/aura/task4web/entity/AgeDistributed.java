package com.aura.task4web.entity;

public class AgeDistributed {
    private Integer id;
    private String cateId;
    private Integer age;
    private Integer ageNums;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCateId() {
        return cateId;
    }

    public void setCateId(String cateId) {
        this.cateId = cateId;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getAgeNums() {
        return ageNums;
    }

    public void setAgeNums(Integer ageNums) {
        this.ageNums = ageNums;
    }
}
